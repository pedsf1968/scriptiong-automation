# Use modules

'''
import math
print(math.pi)
print(math.pow(2,3))
'''

# Use alias
'''
import math as m
print(m.pi)
print(m.pow(2,3))
'''

# Import all the module
'''
from math import *
print(pi)
print(pow(2,3))
'''

# Minimize the number of imported functions
from math import pi,pow
print(pi)
print(pow(2,3))

